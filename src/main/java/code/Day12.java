    private static int findTotalArrangements(String s, String actualGroup, int k, int total) {
        if (!s.contains("?")) {
            List<Integer> list = findBrokenSpringGroups(s);
            String group = list.stream().map(Object::toString).collect(Collectors.joining(","));
            if (group.equals(actualGroup)) {
                total += 1;
            }
        } else {
            for (int i = k; i < s.length(); i++) {
                if (s.charAt(i) == '?') {
                    char[] charArray1 = s.toCharArray();
                    charArray1[i] = '.';
                    String modified1 = new String(charArray1);
                    total = findTotalArrangements(modified1, actualGroup, i + 1, total);

                    char[] charArray2 = s.toCharArray();
                    charArray2[i] = '#';
                    String modified2 = new String(charArray2);
                    total = findTotalArrangements(modified2, actualGroup, i + 1, total);
                }
            }
        }
        return total;
    }

    public static List<Integer> findBrokenSpringGroups(String input) {
        List<Integer> brokenSpringGroups = new ArrayList<>();

        // Check for empty input
        if (input.isEmpty()) {
            return brokenSpringGroups;
        }

        // Initialize variables
        char currentChar = input.charAt(0);
        int currentGroupCount = currentChar == '#' ? 1 : 0;

        // Iterate through the input string
        for (int i = 1; i < input.length(); i++) {
            char nextChar = input.charAt(i);

            // Check if the current and next characters are different
            if (currentChar != nextChar) {
                // Update the count if the current character is '#'
                if (currentChar == '#') {
                    brokenSpringGroups.add(currentGroupCount);
                }
                // Reset the count for the next group
                currentGroupCount = 1;
                currentChar = nextChar;
            } else {
                // Increment the count for the current group
                currentGroupCount++;
            }
        }

        // Add the count of the last group if the last character is '#'
        if (currentChar == '#') {
            brokenSpringGroups.add(currentGroupCount);
        }

        return brokenSpringGroups;
    }
